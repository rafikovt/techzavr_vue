<template>
  <ul class="colors">
              <li class="colors__item"
              v-for="color in colors"
              :key="color.id"
              >
                <label class="colors__label">
                  <input class="colors__radio sr-only"
                  type="radio" name="color"
                  :value="color.id"
                  v-model="currentColorId">
                  <span class="colors__value" :style="{'background-color': color.value}">
                  </span>
                </label>
              </li>
            </ul>
</template>

<script>
export default {
  props: {
    colors: Array,
    colorId: Number,
  },

  computed: {
    currentColorId: {
      get() {
        return this.colorId;
      },
      set(value) {
        this.$emit('update:colorId', value);
      },
    },
  },
};
</script>
